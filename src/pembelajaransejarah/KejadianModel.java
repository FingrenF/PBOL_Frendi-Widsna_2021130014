package pembelajaransejarah;

/**
 *
 * @author Lenovo
 */
public class KejadianModel {
    private String kodeKejadian, namaKejadian, tahunAwal, tahunAkhir, deskripsi;
    
    public String getKodeKejadian() {
        return kodeKejadian;
    }

    public void setKodeKejadian(String kodeKejadian) {
        this.kodeKejadian = kodeKejadian;
    }

    public String getNamaKejadian() {
        return namaKejadian;
    }

    public void setNamaKejadian(String namaKejadian) {
        this.namaKejadian = namaKejadian;
    }

    public String getTahunAwal() {
        return tahunAwal;
    }

    public void setTahunAwal(String tahunAwal) {
        this.tahunAwal = tahunAwal;
    }

    public String getTahunAkhir() {
        return tahunAkhir;
    }

    public void setTahunAkhir(String tahunAkhir) {
        this.tahunAkhir = tahunAkhir;
    }

    public String getDeskripsi() {
        return deskripsi;
    }

    public void setDeskripsi(String deskripsi) {
        this.deskripsi = deskripsi;
    }
    
}
